def unbiased_const(name, n):
    """
    불편화 상수 가져오기 (강의록 08 p.15-16)
    - n < 26: 테이블에서 정확한 값
    - n >= 26: 근사식 사용 (강의록의 d3, d4는 3차항 포함)
    """
    n = int(n)
    if n < 2:
        n = 2
    if n in UNBIASED_TABLE.index and name in UNBIASED_TABLE.columns:
        return float(UNBIASED_TABLE.loc[n, name])

    # n > 25: 근사식 또는 정확 계산
    if name == 'c4':
        # c4(n) = sqrt(2/(n-1)) * gamma(n/2) / gamma((n-1)/2)
        return float(np.sqrt(2/(n-1)) * gamma(n/2) / gamma((n-1)/2))
    if name == 'c2':
        return float(np.sqrt(2/n) * gamma(n/2) / gamma((n-1)/2))
    if name == 'd2':
        return 3.4873 + 0.0250141*n - 0.00009823*n**2
    if name == 'd3':
        # 강의록 page 15: 3차항 포함
        return 0.80818 - 0.051871*n + 0.00005098*n**2 - 0.00000019*n**3
    if name == 'd4':
        return 2.88606 + 0.051313*n - 0.00049243*n**2 + 0.00000188*n**3
    # 나머지는 파생식
    if name == 'A2':
        return 3 / (unbiased_const('d2', n) * np.sqrt(n))
    if name == 'A3':
        return 3 / (unbiased_const('c4', n) * np.sqrt(n))
    if name == 'D3':
        return max(0, 1 - 3*unbiased_const('d3', n)/unbiased_const('d2', n))
    if name == 'D4':
        return 1 + 3*unbiased_const('d3', n)/unbiased_const('d2', n)
    if name == 'B3':
        c4 = unbiased_const('c4', n)
        return max(0, 1 - 3*np.sqrt(1-c4**2)/c4)
    if name == 'B4':
        c4 = unbiased_const('c4', n)
        return 1 + 3*np.sqrt(1-c4**2)/c4
    if name == 'E2':
        return 3 / unbiased_const('d2', n)
    return float(UNBIASED_TABLE.iloc[-1][name])


def grade_capability(cp):
    """공정능력 5등급 판정 (강의록 08 page 9)"""
    if cp >= 1.67:
        return {'grade': 0, 'level': '매우 충분', 'color': '#10b981', 'sigma': '±5σ',
                'action': '들쭉날쭉 약간 켜져도 걱정할 필요가 없다. 비용절감이나 관리의 간소화를 생각하도록 한다.'}
    if cp >= 1.33:
        return {'grade': 1, 'level': '충분', 'color': '#22c55e', 'sigma': '±4σ',
                'action': '아주 이상적인 공정상황이므로 현재의 상태를 유지한다.'}
    if cp >= 1.00:
        return {'grade': 2, 'level': '괜찮음', 'color': '#eab308', 'sigma': '±3σ',
                'action': '공정관리를 확실하게 하여 관리상태를 유지할 것. Cp가 1에 가까워지면 불량발생의 가능성이 있으므로 주의해야 한다.'}
    if cp >= 0.67:
        return {'grade': 3, 'level': '모자란다', 'color': '#f97316', 'sigma': '±2σ',
                'action': '불량품이 생기고 있다. 전체 선별, 공정의 개선, 관리가 필요하다.'}
